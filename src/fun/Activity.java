package fun;

public class Activity {
	private String Activity_id;
	private String Activity_name;
	private String Activity_begintime;
	private String Activity_overtime;
	private String Activity_person;
	private String Activity_content;

	public String getActivity_id() {
		return Activity_id;
	}

	public void setActivity_id(String activity_id) {
		Activity_id = activity_id;
	}

	public String getActivity_name() {
		return Activity_name;
	}

	public void setActivity_name(String activity_name) {
		Activity_name = activity_name;
	}

	public String getActivity_begintime() {
		return Activity_begintime;
	}

	public void setActivity_begintime(String activity_begintime) {
		Activity_begintime = activity_begintime;
	}

	public String getActivity_overtime() {
		return Activity_overtime;
	}

	public void setActivity_overtime(String activity_overtime) {
		Activity_overtime = activity_overtime;
	}

	public String getActivity_person() {
		return Activity_person;
	}

	public void setActivity_person(String activity_person) {
		Activity_person = activity_person;
	}

	public String getActivity_content() {
		return Activity_content;
	}

	public void setActivity_content(String activity_content) {
		Activity_content = activity_content;
	}

}
